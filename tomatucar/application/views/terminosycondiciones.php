Articulo 9 Multas
El conductor principal es el único responsable de las infracciones durante la renta.  Se debe informar al propietario inmediatamente, el conductor principal debe pagar las multas o los gastos antes del regreso.  Si el conductor no puede cargarse de las multas, un pago extra de 500 pesos será retenido además del costo para cubrir el disgusto por el propietario.
Si el propietario recibe una multa a su dirección personal por culpa de una infracción que se produjo durante un periodo de renta, el propietario debe informar TomatuCAR y denunciar el conductor principal. 





