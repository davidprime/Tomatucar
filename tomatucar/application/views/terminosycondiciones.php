Articulo 3 derechos y obligaciones aplicables al conductor
3.1 Condiciones para manejar un vehículo inscrito en TomatuCAR
-	Ser inscrito en TomatuCAR
-	Tener mínimo 18 años
-	Tener un permiso de conducir
-	Tener una carta de crédito
-	No haber tenido responsable de un accidente desde 24 meses.
-	No haber perdido puntos durante 24 meses.
-	No haber tenido rechazo o cancelación de seguro durante los 5 últimos anos.
-	No tener una incapacidad medical que réproba manejar.
-	Informaciones y documentos a proporcionar para manejar un vehículo en TomatuCAR.
-	Fecha de nacimiento.
-	Número de teléfono.
-	Una copia numérica (scan o fotografía) del permiso de conducir que permite de leer; estado civil, el número del permiso de conducir, la lista de los permisos detenido y sus fechas de obtención.
-	Una fotografía visible de una pieza de identidad (credencial de identidad, recto verso o pasaporte).


El conductor debe también registrar su carta bancaria por la primera reservación.

3.2 Obligaciones del conductor principal

El conductor principal es el conductor que hizo la solicitud de renta con el propietario. El conductor principal esta responsable del vehículo del inicio de la renta hasta el final de la renta. El conductor se encarga de usarlo y mantenerlo  durante el tiempo de renta. Solo el conductor inscrito y que ha validado la reservación del vehículo está autorizado a manejar el vehículo.


El conductor principal puede añadir conductor adicional que serán autorizado a conducir el vehículo si y solamente si:

-	Están inscrito en TomatuCAR y cumplen con las condiciones para conducir un vehículo inscrito en TomatuCAR mencionado en el artículo 3.
-	El conductor a previsto inicialmente TomatuCAR vía el sitio o por correo mail a support@tomatucar.com, especificando los nombres, los apellidos de los conductores autorizados y las reservas concernidos.
-	El conductor principal consiguió inicialmente el acuerdo escrito del propietario,  en caso de necesidad usando la mensajería interna del sitio previsto por este efecto. En cuyo caso, el depósito será triplicado en caso de siniestro ocasionado por una persona otra que un conductor debidamente autorizado.

Además, el conductor principal se compromete:

-	Devolver un vehículo proprio y sin nuevos daños no repertorio en el descriptivo del vehículo y en el informe.
-	Devolver el vehículo con el mismo nivel de combustible que al inicio o de arreglar la cantidad correspondiente al combustible consumido por el propietario del vehículo.
-	Respectar los horarios de renta del contrato y la hora de regreso del vehículo.
-	Conducir el vehículo únicamente en los Estados Unidos de México.
-	Pagar la cantidad determinada por la renta, calculado por TomatuCAR (referencia artículo 8).

El conductor principal y cualquier otro conductor autorizado no pueden realquilar el vehículo, remolcar otro vehículo, efectuar el transporte de pasajero contra remuneración, usar el vehículo por tomar parte a manifestaciones deportivas motorizadas, participar a carrera automóviles o cualquier otra competición también para el aprendizaje de manejo.

Articulo 4 derechos y obligaciones aplicables al propietario
4.1 Condiciones para proponer su vehículo a la renta:
-	Estar inscrito en TomatuCAR
-	Ser el propietario del vehículo propuesto a la renta o disponer de la autorización del propietario si el vehículo no le pertenece.
-	Haber escogido un precio de renta.
-	Indicar un lugar habitual de estacionamiento del vehículo propuesto a la renta.
El vehículo debe:
-	Estar matriculado en los Estados-Unidos de México
-	Tener su coche asegurado
-	En buena estado de marcha, sin daño particular y teniendo beneficio de un mantenimiento regular conforme a los leyes y reglamentos en vigor y recomendado por el constructor.
-	Tener una potencia fiscal de 10CV o menos.
-	Tener un valor nuevo inferior a 50 000 euros
Cabe recordar que solo pueden ser garantizados los vehículos terrestres a motor a 4 ruedas, de un tonelaje inferior o igual a 3.5 tonelajes, matriculado en los Estados Unidos de México, excluyendo los buggys y quads.




