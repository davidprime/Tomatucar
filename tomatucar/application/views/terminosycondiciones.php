Articulo 5 transacciones entre miembros de la comunidad TomatuCAR.
Cualquier persona inscrita en el sitio, puede reservar un vehículo en el sitio. Sin embargo, se recuerda que TomatuCAR es un servicio de enlace entre particular. Así, los conductores escogen los vehículos que quieren rentar y los propietarios escogen los miembros a quien prestar sus coches.
TomatuCAR no interviene, de cualquier manera, en su elección.
TomatuCAR actúa siempre en nombre y por la cuenta del propietario y/o del conductor, lo que el propietario y el conductor reconocen expresamente.






Articulo 6 Solicitud, cancelación y prolongación de renta
El conductor se encarga de entregar todas informaciones útiles permitiendo la renta:
-	Fecha y hora de entrega
-	Fecha y hora del regreso
-	Distancia estimado del viaje
-	Motivo y destinación previsto

El propietario se encarga de contestar positivamente, o negativamente lo más rápido posible a cualquier solicitud de renta de un miembro TomatuCAR.
En la salida programada, propietario y arrendador se comprometen que las informaciones comunicadas durante la inscripción todavía sean válidas.
La cancelación de la reserva por el conductor o el propietario es posible sin gastos antes del comienzo de la renta.

Si el conductor principal decide cancelar la renta antes del comienzo de la renta, se le cobrara 10%  de la renta por gestión.

Después de la fecha y la hora prevista del inicio de la renta, la cancelación está posible solamente por notificación del conductor o del propietario por Email o llamada telefónica. En este caso, se cobra la renta por los días ya hecho más 30% del pendiente. En efecto, como la cobertura del seguro esta irrevocablemente iniciado a la fecha de entrega del coche.

Por cualquiera renta, un kilometraje mínimo de 40 km por día será facturado y no reembolsable en caso de kilometraje inferior al fin de la renta.
A la entrega de las llaves, el propietario debe verificar la identidad del conductor gracias a su licencia de conducir.

Propietario y conductor hacen un inventario contradictorio del vehículo llenando el contrato proporcionado por TomatuCAR e imprimido por cualquiera de las partes.
Propietario y conductor conservaran cada uno un ejemplar del inventario. El inventario debe:
-	Notificar todos los daños del vehículo, con una descripción detallada
-	Hacer una lista de los objetos dejado al interior del coche por la comodidad del arrendador (silla de niño etc…)
-	Señalar el kilometraje inicial
-	Señalar el nivel de la reserva del combustible.
El conductor principal está responsable del vehiculó desde que el propietario lo entrego y que umbos firmaron el contrato de renta.
El arrendador se compromete de restituir el coche a la hora y en el lugar convenido.
El fin de la renta está acto durante la restitución del vehículo, la entrega de las llaves y la verificación del inventario del vehículo que debe ser anotado en el contrato de renta y firmado por el propietario y el arrendador. Ambos partes deben guardar un ejemplar del contrato.


El contrato debe imperativamente mencionar:
-	La fecha
-	La hora de regreso
-	El kilometraje final
-	La reserva del combustible
-	Todos los daños del vehículo ocurriendo durante la renta

El propietario debe finalizar la renta en el sitio de la página “mis rentas” en un plazo de 48 horas después de la fecha efectiva del fin de la renta.

El conductor puede informar las informaciones pedidas. En caso de contradicción entre las informaciones transmitía por el conductor y el propietario, TomatuCAR se referirá al inventario firmado por ambos partes es decir propietario y arrendador.
Cualquiera falsificación del contrato concerniente la fecha, la hora de salida y de retorno, y el kilometraje hecho, conduce la no cobertura del seguro, la exclusión del conductor principal y del propietario de la comunidad TomatuCAR.

Sin una finalización en el sitio en un plazo de 48 horas después del fin de la renta, TomatuCAR usara las estimaciones del kilometraje comunicado durante la reserva del conductor principal a fin de facturar y cargar el conductor principal.
Después la facturación efectuada, TomatuCAR adaptara en la medida de lo posible las compensaciones a efectuar en caso de ajustar debito/crédito según las reclamaciones de ambos partes con justificados.

Cada cambio entrena la aplicación de 5 euros de gastos administrativos al propietario.

Por cualquiera prolongación de renta, el conductor principal debe tener el acuerdo de TomatuCAR y del propietario al fin de permitir de mantener  el seguro del coche.

El propietario puede ajustar la nueva hora de regreso del coche a través del sitio en “mis rentas”. Sino el coche del propietario no estará cubierto por el seguro y el conductor del coche será responsable de los danos del vehículo o de un tercero.
Al final de cada renta, los miembros se comprometen a evaluar mutualmente TomatuCAR será el moderador de los comentarios.
Articulo 7 precios de la renta
Todos los precios de los vehículos (días, kilometrajes) son libres. El propietario es el único responsable. TomatuCAR solo puede aconsejar un precio medio permitiendo la renta con arreglo a la antigüedad y el modelo del vehículo.
Los precios anunciados toman en cuenta el seguro del coche y los gastos de funcionamiento de TomatuCAR.
Los precios son calculado con tres variables!
-	Precio del primer día
-	Precio los días siguientes
-	Precio al kilometraje
La renta mínima está fijada a un día.
