Articulo 12 Depósitos
Al momento de la reservación del vehículo, el conductor está informado del importe del depósito que debería pagar en caso de siniestro.
El depósito no está pagado al momento de la reserva pero será retenido en caso de siniestro y eso cualquiera sea la causa. El depósito cubre las reparaciones, gastos de expertos, tranquiza del seguro, gastos de expedientes y cualquiera otro importe procediendo en indemnización por los daños sufridos.
En caso de accidente con recurso parcial contra un tercero, el depósito se aplica por completo si la proporción de los daños no imputables al tercero es superior al importe del depósito.



El importe del depósito esta de...…. Pesos.
En los casos en que la licencia de conducir fue obtenido durante menos de 2 años, el deposito será doblado.
En caso de que el vehículo esta conducido por un conductor que no es el conductor principal o un conductor segundario autorizado por TomatuCAR y el propietario según las condiciones (artículo 3), el depósito será triplicado.
En el caso de un acuso con delito de fuga, el depósito se triplicó. El depósito podría ser devuelto al final del procedimiento si la responsabilidad del conductor que rento el vehículo no sea comprometida.

El importe del depósito será asumido por el conductor principal o la persona garante a través de la tarjeta de crédito en todas las circunstancias.

Articulo 13 Procedimiento en caso de siniestro
Un siniestro se define por una degradación del vehículo.
Entonces durante un siniestro, el conductor debería:

Modificar con las cláusulas del seguro
-	Si es necesario prevenir las autoridades de policías en caso que hay heridos
-	Particularmente
-	Llenar un constato de daño,
-	Prevenir TomatuCAR y el propietario lo más rápido posible por teléfono o por mail, y lo más tarde en las 24 horas después de la restitución del vehículo.

En caso de un accidente por colisión con un vehículo conducido por un tercero, el conductor deberá llenar y firmar el constato de daño con el tercero en el lugar del accidente, lo más tarde en los 24 horas después de la restitución del vehículo.
