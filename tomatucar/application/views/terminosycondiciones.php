-	 Una copia numérica (scan o fotografía) del certifica de inmatriculación.
-	Una ficha de contrato recapitulativo de la renta informada y firmada incluyendo el inventario inicial y final.
-	Una copia del constato de daño
-	Si es posible, una o varias fotos numéricas de daños constatado en el coche.
 
En caso de daño al coche asegurado, el propietario deberá transmitir un presupuesto del importe de la reparación establecido por un profesional o estar constatado por un experto.
El propietario deberá esperar, por proceder o hacer proceder a las reparaciones, al acuerdo de TomatuCAR.
En caso de robo del coche durante la renta, el conductor deberá:
-	Declarar el robo a las autoridades competente en un plazo de 24 horas a la fecha del descubierto del robo.
-	Mandar una copia de la atestación de la queja en un plazo de máximo de 24 horas a TomatuCAR y al propietario.

TomatuCAR deducirá el conductor de 500 pesos de gastos administrativos de gestión de administración de archivos.

